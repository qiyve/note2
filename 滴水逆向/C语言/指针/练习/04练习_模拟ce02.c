int strcmp(char* s1, char* s2)
{
	char* p1 = s1;
	char* p2 = s2;
	int flat = 1;

	while (*p1 == *p2 && *p2!='\0')
	{
		p1++;
		p2++;
	}
	if ( *p2 == '\0')
	{
		flat = 0;
	}
	return flat;
}
