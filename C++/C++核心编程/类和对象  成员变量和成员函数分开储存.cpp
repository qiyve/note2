#include<iostream>
using namespace std;


//成员变量 和  成员函数 分开存储的

class Person
{

	int m_A;//非静态成员变量  属于类的对象上

	static int m_B;//静态成员变量 不属于类对象上

	void func(){}//非静态函数

};

int Person::m_B = 0;  //静态成员变量 特点：类内声明 类外初始化

void test01()
{
	Person p;
	//空对象占用内存空间为 ：1
	//C++编译器会给每一个空对象也分配一个字节空间，是为了区分空对象占内存的位置
	//每个空对象也应该有一个独一无二的内存地址
	cout << "sizeof p =" << sizeof(p) << endl;
}


void test02()
{
	Person p2;
	cout << "sizeof p2 =" << sizeof(p2) << endl;
}
int main()
{
	//test01();

	test02();

	system("pause");
	return 0;
}