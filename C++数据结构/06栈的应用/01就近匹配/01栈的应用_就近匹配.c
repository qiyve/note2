int IsLeft(char c)
{
	return c == '(';
}
int IsRight(char c)
{
	return c == ')';
}

MyChar* CreatMyChar(char* p,int index)
{
	MyChar* mychar = (MyChar*)malloc(sizeof(MyChar));
	mychar->pAddres = p;
	mychar->index = index;

	return mychar;
}

